package org.projectodd.linkfusion;

public class Cheese {
    
    private String name;
    private int age;

    public Cheese(String name, int age) {
        this.name = name;
        this.age = age;
    }
    
    public String getName() {
        return this.name;
    }
    
    public int getAge() {
        return this.age;
    }

}
